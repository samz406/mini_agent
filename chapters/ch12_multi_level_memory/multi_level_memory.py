# ---------------------------------------------------------------------------
# Shared tokeniser helper
# ---------------------------------------------------------------------------

def _tokenise(text: str) -> set[str]:
    """Return a set of tokens from *text* (ASCII words + CJK bigrams/trigrams)."""
    tokens: set[str] = set()
    tokens.update(w.lower() for w in re.findall(r"[a-zA-Z]{2,}", text))
    cjk = re.findall(r"[\u4e00-\u9fff]", text)
    for n in (2, 3):
        for i in range(len(cjk) - n + 1):
            tokens.add("".join(cjk[i : i + n]))
    return tokens


# ---------------------------------------------------------------------------
# MultiLevelMemoryManager — unified facade
# ---------------------------------------------------------------------------

class MultiLevelMemoryManager:
    """Facade that coordinates all four memory tiers.

    Usage pattern (mirrors hermes-agent's memory flow):

      manager = MultiLevelMemoryManager(...)

      # Every turn: update working memory
      manager.working.add("user", user_input)
      manager.working.add("assistant", assistant_reply)

      # On session end: compress working memory into an episode
      manager.close_session(session_id="2026-01-01T12:00:00")

      # Remember a fact about the user
      manager.semantic.remember("user.language", "Python", category="user_profile")

      # At the start of a new session: retrieve relevant context
      past_episodes = manager.episodic.search("Python 排序")
      user_lang = manager.semantic.recall("user.language")
    """

    def __init__(
        self,
        *,
        max_working_messages: int = 20,
        episodic_filepath: str = ".episodic_memory.json",
        semantic_filepath: str = ".semantic_memory.json",
    ) -> None:
        self.working = WorkingMemory(max_messages=max_working_messages)
        self.episodic = EpisodicMemory(filepath=episodic_filepath)
        self.semantic = SemanticMemory(filepath=semantic_filepath)
        self.procedural = ProceduralMemoryRef()

    def close_session(
        self,
        session_id: Optional[str] = None,
        started_at: Optional[str] = None,
    ) -> Episode:
        """Compress the current working memory into an episode and persist it.

        In hermes-agent the LLM generates the summary.  Here we use a
        rule-based approach: concatenate role-prefixed snippets.
        """
        sid = session_id or datetime.now().isoformat()
        sat = started_at or datetime.now().isoformat()

        messages = self.working.get_messages()
        summary = self.working.summarise()

        # Extract keywords: all distinct CJK bigrams and ASCII words from content
        all_text = " ".join(m["content"] for m in messages)
        keywords = sorted(_tokenise(all_text))[:12]

        episode = Episode(
            session_id=sid,
            summary=summary,
            keywords=keywords,
            started_at=sat,
            ended_at=datetime.now().isoformat(),
            message_count=len(messages),
        )
        self.episodic.store(episode)
        self.working.clear()
        return episode

    def context_for_new_session(self, query: str = "", top_episodes: int = 3) -> str:
        """Build a context string to inject at the start of a new session.

        Combines:
          - Relevant past episodes (episodic memory)
          - Key user facts (semantic memory / user profile)

        In hermes-agent this context is injected into the system prompt.
        """
        lines: list[str] = []

        # --- Semantic facts (user profile) ---
        user_facts = self.semantic.by_category("user_profile")
        pref_facts = self.semantic.by_category("preference")
        all_facts = user_facts + pref_facts
        if all_facts:
            lines.append("## 关于用户的已知信息")
            for f in all_facts:
                lines.append(f"- {f.key}: {f.value}  [置信度: {f.confidence:.0%}]")

        # --- Relevant past episodes ---
        if query:
            episodes = self.episodic.search(query, top_k=top_episodes)
        else:
            episodes = self.episodic.get_recent(top_episodes)

        if episodes:
            lines.append("\n## 相关历史会话摘要")
            for ep in episodes:
                lines.append(
                    f"- [{ep.session_id[:19]}] ({ep.message_count} 条消息)\n"
                    f"  {ep.summary[:120]}{'…' if len(ep.summary) > 120 else ''}"
                )

        # --- Procedural hint ---
        lines.append(f"\n## 程序性记忆\n- {ProceduralMemoryRef.INFO}")

        return "\n".join(lines) if lines else "(无历史上下文)"


