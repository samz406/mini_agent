"""Chapter 12: Multi-Level Memory — Four-Tier Agent Memory Architecture.

Teaches:
- Why a single memory store is insufficient for long-lived agents
- The four-tier cognitive memory model adapted for AI agents:
    Working Memory   → current conversation context (in-context, volatile)
    Episodic Memory  → past session summaries (out-of-context, persistent, searchable)
    Semantic Memory  → factual knowledge about the user and world (structured, persistent)
    Procedural Memory → how-to skills (referenced from ch10; pointer pattern shown here)
- How to build a unified MemoryManager facade that decides which tier to query
- How hermes-agent implements each tier in production

Run:
    python multi_level_memory.py
"""

from __future__ import annotations

import json
import os
import re
from collections import deque
from dataclasses import dataclass, field, asdict
from datetime import datetime
from typing import Any, Optional


# ---------------------------------------------------------------------------
# Tier 1 — Working Memory  (current conversation context)
# ---------------------------------------------------------------------------

class WorkingMemory:
    """Short-term, in-context sliding window of the current conversation.

    Backed by a ``collections.deque`` — the oldest messages are automatically
    evicted when the buffer reaches *max_messages*.  Nothing is persisted to
    disk; this memory disappears when the session ends.

    Cognitive analogy: the scratchpad a human keeps in mind while talking.
    """

    def __init__(self, max_messages: int = 20) -> None:
        self._buf: deque[dict[str, str]] = deque(maxlen=max_messages)

    def add(self, role: str, content: str) -> None:
        """Append a message to the working buffer."""
        self._buf.append({"role": role, "content": content})

    def get_messages(self) -> list[dict[str, str]]:
        """Return messages in chronological order (safe to pass to an LLM)."""
        return list(self._buf)

    def clear(self) -> None:
        """Discard all messages (start a new conversation turn)."""
        self._buf.clear()

    def summarise(self) -> str:
        """Produce a brief plain-text summary of the current context.

        In production (hermes-agent) this summary is generated by the LLM and
        then stored into Episodic Memory at session end.
        """
        if not self._buf:
            return "(空对话)"
        lines: list[str] = []
        for msg in self._buf:
            role_label = {"user": "用户", "assistant": "助手", "system": "系统"}.get(
                msg["role"], msg["role"]
            )
            snippet = msg["content"][:60].replace("\n", " ")
            lines.append(f"[{role_label}] {snippet}{'…' if len(msg['content']) > 60 else ''}")
        return "\n".join(lines)

    def __len__(self) -> int:
        return len(self._buf)


# ---------------------------------------------------------------------------
# Tier 2 — Episodic Memory  (past session summaries)
# ---------------------------------------------------------------------------

@dataclass
class Episode:
    """A summarised record of a past conversation session.

    Attributes:
        session_id:  Unique identifier for the session (ISO timestamp by default).
        summary:     LLM-generated (or rule-based) natural-language summary.
        keywords:    Topics discussed — used for keyword-based retrieval.
        started_at:  When the session began (ISO-8601).
        ended_at:    When the session ended / was summarised.
        message_count: Number of messages in the original session.
    """

    session_id: str
    summary: str
    keywords: list[str]
    started_at: str
    ended_at: str = field(default_factory=lambda: datetime.now().isoformat())
    message_count: int = 0

    def to_dict(self) -> dict:
        return asdict(self)

    @classmethod
    def from_dict(cls, data: dict) -> "Episode":
        return cls(**data)


class EpisodicMemory:
    """Persistent store of past session summaries, searchable by keyword.

    Backed by a JSON file.  In hermes-agent this is a SQLite database with an
    FTS5 virtual table for full-text search — here we use keyword matching to
    stay dependency-free.

    Cognitive analogy: autobiographical memory — "last Tuesday we discussed X".
    """

    def __init__(self, filepath: str = ".episodic_memory.json") -> None:
        self.filepath = filepath
        self._episodes: list[Episode] = []
        self._load()

    # --- Persistence ---

    def _load(self) -> None:
        if os.path.exists(self.filepath):
            try:
                with open(self.filepath, "r", encoding="utf-8") as fh:
                    raw: list[dict] = json.load(fh)
                self._episodes = [Episode.from_dict(e) for e in raw]
            except (json.JSONDecodeError, OSError, TypeError):
                self._episodes = []

    def _save(self) -> None:
        with open(self.filepath, "w", encoding="utf-8") as fh:
            json.dump(
                [e.to_dict() for e in self._episodes],
                fh,
                indent=2,
                ensure_ascii=False,
            )

    # --- Write ---

    def store(self, episode: Episode) -> None:
        """Append a new episode and persist."""
        self._episodes.append(episode)
        self._save()

    # --- Read ---

    def get_recent(self, n: int = 5) -> list[Episode]:
        """Return the *n* most-recent episodes (newest last)."""
        return self._episodes[-n:]

    def search(self, query: str, top_k: int = 3) -> list[Episode]:
        """Return up to *top_k* episodes whose summary or keywords match *query*.

        Scoring:
          - Each keyword match in episode.keywords   → +2 pts
          - Each query word found in episode.summary → +1 pt
          CJK text uses bigram/trigram matching.
        """
        query_tokens = _tokenise(query)
        scored: list[tuple[float, Episode]] = []

        for ep in self._episodes:
            score = 0.0
            ep_kw_tokens = {kw.lower() for kw in ep.keywords}
            for qt in query_tokens:
                if qt in ep_kw_tokens:
                    score += 2
            summary_tokens = _tokenise(ep.summary)
            score += len(query_tokens & summary_tokens)
            if score > 0:
                scored.append((score, ep))

        scored.sort(key=lambda x: -x[0])
        return [ep for _, ep in scored[:top_k]]

    def list_all(self) -> list[Episode]:
        return list(self._episodes)


# ---------------------------------------------------------------------------
# Tier 3 — Semantic Memory  (factual knowledge about the user and world)
# ---------------------------------------------------------------------------

@dataclass
class SemanticFact:
    """A single piece of factual knowledge.

    Attributes:
        key:        Short unique identifier, e.g. "user.language".
        value:      The fact itself, e.g. "Python".
        category:   Logical grouping, e.g. "user_profile", "preference", "world".
        confidence: 0.0–1.0; lower values mean the fact may be stale or uncertain.
        updated_at: ISO-8601 timestamp of the last update.
        source:     Where this fact came from, e.g. "user_stated", "inferred".
    """

    key: str
    value: Any
    category: str = "general"
    confidence: float = 1.0
    updated_at: str = field(default_factory=lambda: datetime.now().isoformat())
    source: str = "user_stated"

    def to_dict(self) -> dict:
        return asdict(self)

    @classmethod
    def from_dict(cls, data: dict) -> "SemanticFact":
        return cls(**data)


class SemanticMemory:
    """Persistent key-value store for structured factual knowledge.

    Unlike EpisodicMemory (which stores *what happened*), SemanticMemory stores
    *what is true* — user preferences, domain facts, world knowledge snippets.

    In hermes-agent this is partly implemented by MEMORY.md (flat text) and
    partly by Honcho's dialectic user-modeling service.

    Cognitive analogy: long-term declarative memory — "Python is a language".
    """

    def __init__(self, filepath: str = ".semantic_memory.json") -> None:
        self.filepath = filepath
        self._facts: dict[str, SemanticFact] = {}
        self._load()

    # --- Persistence ---

    def _load(self) -> None:
        if os.path.exists(self.filepath):
            try:
                with open(self.filepath, "r", encoding="utf-8") as fh:
                    raw: dict = json.load(fh)
                self._facts = {k: SemanticFact.from_dict(v) for k, v in raw.items()}
            except (json.JSONDecodeError, OSError, TypeError):
                self._facts = {}

    def _save(self) -> None:
        with open(self.filepath, "w", encoding="utf-8") as fh:
            json.dump(
                {k: v.to_dict() for k, v in self._facts.items()},
                fh,
                indent=2,
                ensure_ascii=False,
            )

    # --- Write ---

    def remember(
        self,
        key: str,
        value: Any,
        category: str = "general",
        confidence: float = 1.0,
        source: str = "user_stated",
    ) -> None:
        """Store or update a fact."""
        self._facts[key] = SemanticFact(
            key=key,
            value=value,
            category=category,
            confidence=confidence,
            source=source,
        )
        self._save()

    def forget(self, key: str) -> None:
        """Remove a fact by key."""
        self._facts.pop(key, None)
        self._save()

    # --- Read ---

    def recall(self, key: str) -> Optional[Any]:
        """Return the value for *key*, or ``None`` if absent."""
        fact = self._facts.get(key)
        return fact.value if fact else None

    def recall_fact(self, key: str) -> Optional[SemanticFact]:
        """Return the full SemanticFact record for *key*."""
        return self._facts.get(key)

    def by_category(self, category: str) -> list[SemanticFact]:
        """Return all facts in the given *category*, sorted by key."""
        return sorted(
            [f for f in self._facts.values() if f.category == category],
            key=lambda f: f.key,
        )

    def search(self, query: str) -> list[SemanticFact]:
        """Return facts whose key or string-value contains *query* (case-insensitive)."""
        q = query.lower()
        return [
            f for f in self._facts.values()
            if q in f.key.lower() or q in str(f.value).lower()
        ]

    def get_all(self) -> dict[str, SemanticFact]:
        return dict(self._facts)


